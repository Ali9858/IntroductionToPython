# Создаем список простых чисел
primes = [2, 3, 5, 7, 11, 13]

# Распечатываем 2-е, 4-е и 6-е числа в списке (индексы 1, 3 и 5)
print(primes[1])  # Второе число (индекс 1)
print(primes[3])  # Четвертое число (индекс 3)
print(primes[5])  # Шестое число (индекс 5)

#Ссылка на список
a = [1, 2, 3, 4]
b = a  # b ссылается на тот же список, что и a

b[0] = 0  # Изменяем первый элемент списка через b

# Печатаем оба списка
print("a:", a)  # Выведет a: [0, 2, 3, 4]
print("b:", b)  # Выведет b: [0, 2, 3, 4]

#Копирование списка
a = [1, 2, 3, 4]
b = list(a)  # Создаем копию списка a

b[0] = 0  # Изменяем первый элемент в списке b

# Печатаем оба списка
print("a:", a)  # Выведет a: [1, 2, 3, 4]
print("b:", b)  # Выведет b: [0, 2, 3, 4]

#Сложение двумерных векторов
def add_vector(v, w):
    # Сложение векторов по элементам
    return [v[0] + w[0], v[1] + w[1]]

# Пример
v = [1, 2]
w = [3, 4]
result = add_vector(v, w)
print(result)  # Выведет [4, 6]

